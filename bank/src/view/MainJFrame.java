    private void setCustomContentPanel(JPanel panel) {
        panel.setSize(250, 170);
        panel.setLocation(0, 0);

        jPanelContainer.removeAll();
        jPanelContainer.add(panel, BorderLayout.CENTER);
        jPanelContainer.revalidate();
        jPanelContainer.repaint();

        pack();
    }
    
